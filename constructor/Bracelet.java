    String material;
    String color;
    double price;
    double weight;
    double length;
    String claspType;
    String design;
    boolean isAdjustable;
    String stoneType;
    int numberOfStones;
    String finish;
    String style;
    boolean isWaterproof;
    String occasion;
    String gender;
