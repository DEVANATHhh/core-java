class Racket
{
    String brand;
    String type;
    double weight;
    double length;
    String gripType;
    String material;
    String color;
    double price;
    String stringType;
    boolean isStrung;
	
	Racket(String brand,String type,double weight,double length,String gripType,String material,
	String color,double price,String stringType,boolean isStrung)
	{
		this.brand=brand;
		this.type=type;
		this.weight=weight;
		this.length=length;
		this.gripType=gripType;
		this.material=material;
		this.color=color;
		this.price=price;
		this.stringType=stringType;
		this.isStrung=isStrung;
	}
}