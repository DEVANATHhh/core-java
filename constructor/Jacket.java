class Jacket
{
    String brand;
    String size;
    String color;
    double price;
    String material;
	
	Jacket(String brand,String size,String color,double price,String material)
	{
		this.brand=brand;
		this.size=size;
		this.color=color;
		this.price=price;
		this.material=material;
	}
}