class Bucket
{
	double capacity;
	String color;
    String material;
    String shape;
    boolean hasHandle;
	
	Bucket(double capacity,String color,String material,String shape,boolean hasHandle)
	{
		this.capacity=capacity;
		this.color=color;
		this.material=material;
		this.shape=shape;
		this.hasHandle=hasHandle;
	}
}