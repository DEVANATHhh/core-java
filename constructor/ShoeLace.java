class ShoeLace
{
    String brand;
    String color;
    double length;
    String material;
    String pattern;
    double price;
    boolean isElastic;
    String shape;
    String thickness;
    int stock;
	
	ShoeLace(String brand,String color,double length,String material,String pattern,double price,
	boolean isElastic,String shape,String thickness,int stock)
	{
		this.brand=brand;
		this.color=color;
		this.length=length;
		this.material=material;
		this.pattern=pattern;
		this.price=price;
		this.isElastic=isElastic;
		this.shape=shape;
		this.thickness=thickness;
		this.stock=stock;
	}
}