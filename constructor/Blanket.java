class Blanket 
{
    String material;
    String size;
    String color;
    double price;
    double weight;
    boolean isWashable;
	
	Blanket(String material,String size,String color,double price, double weight,boolean isWashable)
	{
		this.material=material;
		this.size=size;
		this.color=color;
		this.price=price;
		this.weight=weight;
		this.isWashable=isWashable;
	}
}